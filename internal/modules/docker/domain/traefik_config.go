package domain

import (
	"fmt"
	"strings"
)

// GenerateTraefikConfig produces a Traefik file provider dynamic config (YAML)
// from the network's stacks and their routes. The result can be downloaded and
// volume-mounted into a Traefik container as a file provider source.
func GenerateTraefikConfig(network *SwarmNetwork, stacks []*SwarmStack, routes []*SwarmRoute) string {
	// Index routes by stackID for fast lookup
	routesByStack := make(map[string][]*SwarmRoute)
	for _, r := range routes {
		routesByStack[r.StackID] = append(routesByStack[r.StackID], r)
	}

	var sb strings.Builder
	sb.WriteString("# Generated by VVS — do not edit manually\n")
	sb.WriteString(fmt.Sprintf("# Network: %s (%s)\n\n", network.Name, network.Subnet))
	sb.WriteString("http:\n  routers:\n")

	hasRouters := false
	for _, stack := range stacks {
		stackRoutes := routesByStack[stack.ID]
		for _, route := range stackRoutes {
			hasRouters = true
			routerName := sanitizeLabel(fmt.Sprintf("%s-%s", stack.Name, route.Hostname))
			sb.WriteString(fmt.Sprintf("    %s:\n", routerName))
			sb.WriteString(fmt.Sprintf("      rule: \"Host(`%s`)\"\n", route.Hostname))
			sb.WriteString(fmt.Sprintf("      service: %s\n", routerName))
			if route.StripPrefix {
				sb.WriteString(fmt.Sprintf("      middlewares: [%s-strip]\n", routerName))
			}
		}
	}
	if !hasRouters {
		sb.WriteString("    {} # no routes defined\n")
	}

	sb.WriteString("\n  services:\n")
	hasServices := false
	for _, stack := range stacks {
		stackRoutes := routesByStack[stack.ID]
		for _, route := range stackRoutes {
			hasServices = true
			routerName := sanitizeLabel(fmt.Sprintf("%s-%s", stack.Name, route.Hostname))
			sb.WriteString(fmt.Sprintf("    %s:\n", routerName))
			sb.WriteString("      loadBalancer:\n")
			sb.WriteString("        servers:\n")
			sb.WriteString(fmt.Sprintf("          - url: \"http://%s:%d\"\n", stack.Name, route.Port))
		}
	}
	if !hasServices {
		sb.WriteString("    {} # no services defined\n")
	}

	sb.WriteString("\n  middlewares:\n")
	hasMiddleware := false
	for _, stack := range stacks {
		stackRoutes := routesByStack[stack.ID]
		for _, route := range stackRoutes {
			if !route.StripPrefix {
				continue
			}
			hasMiddleware = true
			routerName := sanitizeLabel(fmt.Sprintf("%s-%s", stack.Name, route.Hostname))
			sb.WriteString(fmt.Sprintf("    %s-strip:\n", routerName))
			sb.WriteString("      stripPrefix:\n")
			sb.WriteString("        prefixes: [\"/\"]\n")
		}
	}
	if !hasMiddleware {
		sb.WriteString("    {} # no middlewares defined\n")
	}

	return sb.String()
}

func sanitizeLabel(s string) string {
	var out strings.Builder
	for _, c := range s {
		if (c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') || (c >= '0' && c <= '9') || c == '-' {
			out.WriteRune(c)
		} else {
			out.WriteRune('-')
		}
	}
	return strings.Trim(out.String(), "-")
}
